package com.TrainingRite;

public class Variables {
	
	public static void main(String[] args) {
		String name = "Alec";
		//System.out.println(name);
		System.out.println("My name is " + name);
		
		//Variables
		//1. Strings - series of characters
		//2. Double - a large number and can take decimals (Long, Float, Short)
		//3. Integer - whole numbers (neg or pos)
		//4. Boolean - true or false
		
		//Compute Salary
		double salaryRate = 30.50;
		int numberOfHoursWorked = 40;
		double income = salaryRate * numberOfHoursWorked;
		System.out.println(income);		
	}
	
}
